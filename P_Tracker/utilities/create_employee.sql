SELECT
    BADGE_ID,
    FULL_NAME,
    BUSINESS_TITLE,
    SUPERVISOR_BADGE_ID_1
FROM HR.T_EMPLOYEE
WHERE TERMINATED = 0
AND WORK_EMAIL_ADDRESS != 'NULL'
AND (COST_CENTER_ID = '3400'
      OR COST_CENTER_ID = '3470'
      OR COST_CENTER_ID = '3700')
AND (SUPERVISOR_BADGE_ID_1 = '104550'
    OR SUPERVISOR_BADGE_ID_2 = '104550'
    OR SUPERVISOR_BADGE_ID_3 = '104550'
    OR SUPERVISOR_BADGE_ID_4 = '104550'
    OR SUPERVISOR_BADGE_ID_5 = '104550'
    OR SUPERVISOR_BADGE_ID_6 = '104550'
    OR SUPERVISOR_BADGE_ID_7 = '104550'
    OR SUPERVISOR_BADGE_ID_8 = '104550'
    OR SUPERVISOR_BADGE_ID_9 = '104550'
    OR SUPERVISOR_BADGE_ID_10 = '104550'
    OR BADGE_ID = '104550')
ORDER BY FULL_NAME;